import mongoose from 'mongoose';

export const itemSchema=mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    category:{
        type:String,
        required:true
    },
    description:{
        type:String,
        required:true
    },
    price:{
        type:Number,
        required:true
    },
    image:{
        type:String,
        required:true
    }
})
const itemModel=mongoose.model("Item",itemSchema);

export default itemModel;